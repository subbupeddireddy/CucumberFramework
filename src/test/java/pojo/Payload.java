package pojo;

public class Payload {
	
	
	private int id;
	private String packType;
	private String prodCode;
	private String prodName;
	private String prodSize;
	private String status;
	private String upc;
	private String mrpBatch;
	private double mrp;
	private double prodWeight;
	private String prodVolume;
	private String volumeUnit;
	private String weightUnit;
	private String createdOn;
	private String brandLabelStatus;

	public int getId() {
		return id;
	}
	public String getPackType() {
		return packType;
	}
	public String getProdCode() {
		return prodCode;
	}
	public String getProdName() {
		return prodName;
	}
	public String getProdSize() {
		return prodSize;
	}
	public String getStatus() {
		return status;
	}
	public String getUpc() {
		return upc;
	}
	public String getMrpBatch() {
		return mrpBatch;
	}
	public double getMrp() {
		return mrp;
	}
	public double getProdWeight() {
		return prodWeight;
	}
	public String getProdVolume() {
		return prodVolume;
	}
	public String getVolumeUnit() {
		return volumeUnit;
	}
	public String getWeightUnit() {
		return weightUnit;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public String getBrandLabelStatus() {
		return brandLabelStatus;
	}

}
